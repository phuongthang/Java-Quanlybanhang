    private void tableMatHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMatHangMouseClicked
        int id = tableMatHang.getSelectedRow();
        if(id>=0)
        {
            txtMaMH.setText((String) model.getValueAt(id, 1));
            txtTenMH.setText((String) model.getValueAt(id, 2));
            txtMota.setText((String) model.getValueAt(id, 5));
            txtDongia.setText((String) model.getValueAt(id, 4));
            cbLoai.setSelectedItem((String) model.getValueAt(id, 3));
            txtMaMH.setEditable(false);
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_tableMatHangMouseClicked

