    public final void getData()
    {
        model.setNumRows(0);
        list = new DAO_Mathang().getListMHN();
        int i = 1;
        for(MatHang obj:list)
        {
            model.addRow(new Object[]{
            i++, obj.getMahangnhap(),obj.getTenmathang(),obj.getLoai(),obj.getDongia(),obj.getMota()
            });
        }
        txtMaMH.setEnabled(true);
        
    }
