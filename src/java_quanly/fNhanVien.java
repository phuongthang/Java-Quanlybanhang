    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        NhanVien nv = new NhanVien();
        nv.setManv(txtMaNV.getText());
        nv.setHoten(txtHoTen.getText());
        if(rdNam.isSelected())
        {
            nv.setGioitinh("Nam");
        }
        else
        {
            nv.setGioitinh("Nữ");
        }
        nv.setQuequan(cbQue.getItemAt(cbQue.getSelectedIndex()));
        nv.setSdt(txtSdt.getText());
        try {
            nv.setNgaySinh(new SimpleDateFormat("dd/MM/yyyy").parse(txtNgaySinh.getText()));
        } catch (ParseException ex) {
        }
        list.add(nv);
        if(new DAO_NhanVien().addNhanVien(nv))
        {
            JOptionPane.showMessageDialog(rootPane, "Thành công !");
            getData();
            clearText();
            getDate();
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Đã tồn tại mã sinh viên");
        }
    }//GEN-LAST:event_btnThemActionPerformed

