    private void tableNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableNhanVienMouseClicked
        int id = tableNhanVien.getSelectedRow();
        if(id>=0)
        {
            txtMaNV.setText((String) model.getValueAt(id, 1));
            txtHoTen.setText((String) model.getValueAt(id, 2));
            if("Nam".equals((String) model.getValueAt(id, 3)))
            {
                rdNam.setSelected(true);
            }
            else
            {
                rdNu.setSelected(true);    
            }
            cbQue.setSelectedItem((String) model.getValueAt(id, 4));
            txtSdt.setText((String) model.getValueAt(id, 6));
            DateFormat df = new SimpleDateFormat("dd/MM/yyyy");  
            String text = df.format(model.getValueAt(id, 5));
            txtNgaySinh.setText(text);
            txtMaNV.setEnabled(false);
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_tableNhanVienMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int result = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn xóa không","Question",JOptionPane.YES_NO_OPTION);
        if(result==JOptionPane.YES_OPTION)
        {
            String manv = txtMaNV.getText();
            if(new DAO_NhanVien().deleteNhanVien(manv))
            {
                JOptionPane.showMessageDialog(rootPane, "Xóa thành công !","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                getData();
                clearText();
                getDate();
                btnThem.setEnabled(true);
                btnSua.setEnabled(false);
                btnXoa.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String truyvan = txtTimKiem.getText();
        list = new DAO_NhanVien().findNhanVien(truyvan);
        model.setNumRows(0);
        int i = 1;
        for(NhanVien obj:list)
        {
            model.addRow(new Object[]{
            i++, obj.getManv(),obj.getHoten(),obj.getGioitinh(),obj.getQuequan(),obj.getNgaySinh(),obj.getSdt()
            });
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

