    public final void getData()
    {
        model.setNumRows(0);
        list = new DAO_NhanVien().getListNhanVien();
        int i = 1;
        for(NhanVien obj:list)
        {
            model.addRow(new Object[]{
            i++, obj.getManv(),obj.getHoten(),obj.getGioitinh(),obj.getQuequan(),obj.getNgaySinh(),obj.getSdt()
            });
        }
        txtMaNV.setEnabled(true);
    }
