    public void XuatHoaDon(String mahd)
    {
        try {
            
            Hashtable map = new Hashtable();
            JasperReport report = JasperCompileManager.compileReport("src/java_quanly/rpHoaDon.jrxml");
            
            map.put("MaHD", mahd);
            JasperPrint p = JasperFillManager.fillReport(report,  map, DriverManager.getConnection(DAO_Connection.url + DAO_Connection.username + DAO_Connection.password));
            JasperViewer.viewReport(p, false);
            JasperExportManager.exportReportToPdfFile(p, "test.pdf");
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }
