    private void rdKhacItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_rdKhacItemStateChanged
        if(evt.getStateChange()==ItemEvent.SELECTED)
        {
            String giamgia = JOptionPane.showInputDialog(null,"Muốn giảm giá bao nhiêu ??","Giảm giá",JOptionPane.QUESTION_MESSAGE);
            while(Integer.parseInt(giamgia) > 100)
            {
                giamgia = JOptionPane.showInputDialog(null,"Muốn giảm giá bao nhiêu ??","Giảm giá",JOptionPane.QUESTION_MESSAGE);
            }
            int tongtien = tienban - (tienban * Integer.parseInt(giamgia))/100;
            txtTongTien.setText(String.valueOf(tongtien));
            persent = giamgia;
        }
        
    }//GEN-LAST:event_rdKhacItemStateChanged

